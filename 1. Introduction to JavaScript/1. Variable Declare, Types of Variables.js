// Number data type
let money = 255;
console.log(money);

// String data type
let degree = "BSC in CSE";
console.log(degree);

// Boolean data type
let ismad = true;
let ispandit = false;

console.log(ismad);
console.log(ispandit);


//How to get the name of the data_type of a variable
console.log(typeof money);
console.log(typeof degree);
