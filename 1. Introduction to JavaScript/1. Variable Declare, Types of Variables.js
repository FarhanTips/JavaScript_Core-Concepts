
// Primitive way to declare a variable
var quantity = 35;

console.log(quantity);


// Advanced way to declare variables
let name = "Farhan Akbor Khan";    // When we use let, means the value of the variable can be changed in future if needed.

const doB = "2nd June, 2002";      // When we use const, means the value of the variable is fixed. Cannot be changed in future.

console.log(name);
console.log(doB);

name = "Jhankar";
console.log(name);

