
// Practise Task-13

// for loop

for (let i = 1; i <= 5; i++) {
    console.log(i);
}


// while loop

let x = 1;

while (x <= 5) {
    console.log(x);
    x++;
}



// do while loop

let y = 1;

do {
    console.log(y);
    y++;
} while (y <= 5);



/**
 * 
 * 
 * Which one should you use?
 * 
for → Best when you know how many times the loop should run (like printing 1 to 5).

while → Best when you don't know in advance how many times you'll loop; you just keep going until a condition becomes false.

do...while → Best when the code must run at least once, even if the condition is false initially.

 */
